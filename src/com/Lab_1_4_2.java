package com;

public class Lab_1_4_2 {

    public static void main(String[] args) {

        // Write a console program that prints result of each of arithmetic operations (+, -, /, *, %)
        // for two variables of primitive data types.

        int a = 5, b = -2;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}